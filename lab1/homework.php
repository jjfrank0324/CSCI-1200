<h3>Homework Due Date and Time</h3>

<p> Homework assignments are due Thursday nights at 11:59pm.  Homework
is submitted electronically and submission times are judged by the
webserver timestamp.  If your submission is one minute late, it is a
day late, so we suggest that you don't actually wait until the last
minute.  You may submit your assignment multiple times -- we will only
grade your last submission (or you may specify which version we should
grade).  Please see the <a href="calendar.php">Calendar</a> for the
homework assignments and due dates.  </p>

<p> <font class="italics">Note: Please do not email your homework
assignment to the instructor or the TAs.  We will only grade material
submitted to the homework submission server.  We can login and view
your submitted homework files if you encounter a problem at submission
time and have a short question related to homework submission.
</font> </p>

<h3>Late Day Policy</h3>

<p>
Each student will initially be given one day (whole or partial) of
grace for late homework assignments.  Through consistent participation
and solid performance on in-lecture <a href="iclicker.php">iClicker
exercises</a> each student <em>may earn additional late days</em>.
These grace days are intended to cover minor illnesses, hardware
malfunctions, and schedule conflicts with homeworks/tests in your
other classes.
</p>

<p>
To use a late day, simply submit the assignment as normal through the
homework submission link.  You do not need to notify your TA or
instructor.  For example, an assignment submitted 22 hours late
(Friday evening at 10:00pm) counts as 1 late day used.  As another
example, an assignment submitted 26 hours late (Saturday morning at
2:00am) counts as 2 late days used.  <em>No more than two late days
may be used for any one assignment.</em>  Once your late days have
been exhausted, late assignments will not be accepted without an
excused absense note from the 
<a href="http://studentlife.rpi.edu/student-experience/excused-absence"> Student
Experience</a> office.</em> </p>

<p>
Students should use their late days carefully, saving them for the
latter part of the semester or (better yet) not using them at all.
</p>


<h3>Electronic Submission</h3> 

<p>
Make sure you read and follow the <a href="academic_integrity.php">
Collaboration Policy and Academic Integrity</a> statement which
applies to all homework assignments.  Once you've finished writing,
testing, debugging, and commenting your code, prepare your assignment
for submission as described below.
</p>

<p>Re-read the assignment description and collect all of the necessary
files for submission.  Be sure to include your plaintext README.txt
(use the provided template for the assignment), your .cpp and .h code
files, and any other files specifically requested for the assignment.
You do not need to include any compiled object files or the
executable.  The server will ignore these files if they are included.
You do not need to include any of the provided input data files or
your output files for the basic homework --- these will be
re-generated by the server.  But you should include any new data input
files and corresponding output for any extra credit you completed, as
described in the assignment description.
</p>

<p>You may upload files to the website by selecting or dragging
multiple files to the appropriate location on the assignment page.  Or
you may create a zip file that contains the necessary files.  Do not
use any other compression or archiving format as the server will not
know how to uncompress your files.  Do not zip up any directories or
folders (unless instructed for the assignment).  Just directly zip the
files.
</p>

<ul>

<li>
<p>
How to zip files using the Cygwin/Linux/FreeBSD/UNIX/MacOSX command line:
</p>

<pre>
zip hw1_submit.zip README.txt myfile.cpp myotherfile.cpp myheaderfile.h
</pre>

<p>
Replace <tt>myfile.cpp myotherfile.cpp myheaderfile.h</tt> with your
actual file names.  Note you can use <tt>*.cpp *.h</tt> to indicate
all .cpp and .h files in the current directory.
</p>


<li><p> If you're using Windows, you can alternatively use WinZip to
create a new archive named <tt>hw1_submit.zip</tt> and then drag &
drop the files the archive.  Again, don't zip up the folder
containing the assignment files, just zip up the files.
</p>

</ul>

<p> Next click the submission link below.  <em>You will use your RCS
ID and password to access this page.</em>  Contact the instructor if
you are registered for the course and cannot successfully login to the
homework submission server.
</p>


