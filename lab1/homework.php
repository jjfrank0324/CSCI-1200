<h3>Homework Submission Server Automatic Grading</h3>

<p> The homework submission server will compile your code with g++
4.8.X and then run the resulting executable on several test cases.
It will inform you of any compile, link, and runtime errors or
warnings and will present the results of these tests for your review.
Your grade for the homework will be a combination of this online
testing of your code and (later) a TA's review of the structure,
efficiency, and overall quality of your code (see
also <a href="hw_grading_criteria.php">homework grading criteria</a>
and
<a href="advice_TAs.php">advice from TAs</a>), plus the TA's
grading of any additional non-code parts of the assignment.  </p>

<p>You will have practice using the submission process during the
first lab.  Please see a TA during office hours if you need additional
help writing portable code.  
</p>


<p>Carefully check the result of your submission.  Verify that all
necessary files were received by the server.  Confirm that your code
compiled and ran successfully.  If you find a mistake in your
homework, you can fix it and re-submit using the same procedure.  We
will only grade the most recent submission.  Please see a TA in office
hours if you need help preparing your assignment for successful
submission.  If the automatic grading of your homework seems incorrect
or unfair please see a TA in office hours.  You may also make a note
to the TAs in your README.txt file asking them to take another look at
your output.  The TAs are able to adjust the automatic grading if
necessary (but this is rare).
</p>


<p><b>Note on # of submissions:</b> Do not use the homework server as
your sole development/testing platform.  You should use your laptop as
your primary testing & debugging interface.  However, don't wait until
the last minute to submit for the first time.  You should do an early
practice submission of a partially working version of the homework to
confirm you are on the right track.  A small point penalty will be
assessed if the count of your submission attempts is greater than 20
for any one homework assignment.  </p>

<p><b>Note on submission file size:</b> If you accidentally include
lots of unnecessary "stuff" in your uploaded files, it may be too big
for submission.  If you think you have included only the necessary
files and yet the server still complains about the size of your
submission, contact the instructor -- <em>but please don't send your
homework files by email!</em> </p>


<p><b>Note on the submission server:</b> If the homework server
crashes or is unresponsive, or if your submission attempt fails and
you cannot make sense of the submission server output, check the <a
href="announcements.php">announcements</a> page and <a
href="http://lms9.rpi.edu/">LMS</a> to see if it is a
known submission server problem.  Otherwise, contact the instructor
and your graduate lab TA for help -- <em>but please don't send your
homework files by email!</em>

</p>

